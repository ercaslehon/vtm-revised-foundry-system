export function normalizeTrechkalovName(value = "") {
  return String(value ?? "")
    .trim()
    .toLowerCase()
    .replaceAll("ё", "е")
    .replace(/[«»"']/g, "")
    .replace(/[\\/_-]+/g, " ")
    .replace(/\s+/g, " ");
}

