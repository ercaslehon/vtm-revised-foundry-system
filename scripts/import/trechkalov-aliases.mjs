export function resolveTrechkalovAlias(category, value, fallback = value) {
  const raw = String(value ?? "").trim();
  if (!raw) return fallback ?? "";

  const aliases = CATEGORY_ALIASES[category] ?? {};
  const normalized = normalizeTrechkalovName(raw);

  return aliases[normalized] ?? raw;
}

export function getTrechkalovAliasTable(category) {
  return { ...(CATEGORY_ALIASES[category] ?? {}) };
}
